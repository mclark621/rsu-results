/// Returns a reader-friendly label for [shortCode] when known; otherwise returns [shortCode] unchanged.
String rsuAgeGroupDisplayLabel(String shortCode) {
  final trimmed = shortCode.trim();
  if (trimmed.isEmpty) return trimmed;

  final direct = rsuAgeGroupDisplayNames[trimmed];
  if (direct != null) return direct;

  final lower = trimmed.toLowerCase();
  for (final e in rsuAgeGroupDisplayNames.entries) {
    if (e.key.toLowerCase() == lower) return e.value;
  }

  return trimmed;
}
